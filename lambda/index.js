const Alexa = require('ask-sdk-core');

const SaludoIntentHandler = {
    canHandle(handlerInput) {
        return Alexa.getRequestType(handlerInput.requestEnvelope) === 'IntentRequest'
            && Alexa.getIntentName(handlerInput.requestEnvelope) === 'SaludoIntent';
    },
    handle(handlerInput) {
        const speakOutput = 'Hola, saludos desde la universidad tecnológica de Xicotepec de Juárez.';
return handlerInput.responseBuilder.speak(speakOutput).reprompt('Puedes preguntarme quién creó esta skill, qué carrera estudia, cual es su color favorito o cual es su musica favorita.').getResponse();
    }
};

const InfoIntentHandler = {
    canHandle(handlerInput) {
        return Alexa.getRequestType(handlerInput.requestEnvelope) === 'IntentRequest'
            && Alexa.getIntentName(handlerInput.requestEnvelope) === 'InfoIntent';
    },
    handle(handlerInput) {
        const infoSlot = Alexa.getSlotValue(handlerInput.requestEnvelope, 'infoTipo');
        
        let speakOutput = '';

        switch (infoSlot) {
            case 'creo':
                speakOutput = 'Esta skill fue creada por Alina Bonilla Paredes.';
                break;
            case 'carrera':
                speakOutput = 'Alina estudia el noveno cuatrimestre de la Ingeniería en Desarrollo de Software Multiplataforma.';
                break;
            case 'color favorito':
                speakOutput = 'El color favorito de Alina es el rosa pastel.';
                break;
            case 'grupo o cantante favorito':
                speakOutput = 'A Alina le gusta la música de Adele y Carla Morrison, aunque escucha de todo.';
                break;
            default:
                speakOutput = 'No tengo información sobre eso. Puedes preguntarme quien creó la aplicación, qué carrera estudia, cuál es su su color favorito o su grupo o cantante favorito.';
        }

        return handlerInput.responseBuilder
            .speak(speakOutput)
            .reprompt('¿Te gustaría saber otra cosa sobre Alina?')
            .getResponse();
    }
};

const HelpIntentHandler = {
    canHandle(handlerInput) {
        return Alexa.getRequestType(handlerInput.requestEnvelope) === 'IntentRequest'
            && Alexa.getIntentName(handlerInput.requestEnvelope) === 'AMAZON.HelpIntent';
    },
    handle(handlerInput) {
        const speakOutput = 'You can say hello to me! How can I help?';

        return handlerInput.responseBuilder
            .speak(speakOutput)
            .reprompt(speakOutput)
            .getResponse();
    }
};

const CancelAndStopIntentHandler = {
    canHandle(handlerInput) {
        return Alexa.getRequestType(handlerInput.requestEnvelope) === 'IntentRequest'
            && (Alexa.getIntentName(handlerInput.requestEnvelope) === 'AMAZON.CancelIntent'
                || Alexa.getIntentName(handlerInput.requestEnvelope) === 'AMAZON.StopIntent');
    },
    handle(handlerInput) {
        const speakOutput = 'Goodbye!';

        return handlerInput.responseBuilder
            .speak(speakOutput)
            .getResponse();
    }
};
